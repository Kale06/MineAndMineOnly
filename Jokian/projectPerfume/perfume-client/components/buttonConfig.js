const buttons = document.querySelectorAll('button');

buttons.forEach(button => {
    button.addEventListener('click', () => {
        const name = button.getAttribute('name');
        checkNote(name);
    });
});

const contentItem = document.querySelector('.banner');


function removeNote() {
    const noteItem = document.querySelector('.note-item');
    if (noteItem) {
        noteItem.remove();
    }
}


function checkNote(name) {
    removeNote();
    const selectedItem = items.find(item => item.type.toLowerCase() === name.toLowerCase());
    console.log(name, selectedItem);
    if (!selectedItem) return;

    const htmlTemplate = `
        <div class="card card-content note-item">
            <img class="small-square-item" src="../../notes/citrus/main.png" alt="" srcset="">
            <h3>${selectedItem.type}</h3>
            <p>${selectedItem.typeDesc}</p>
            <div class="items"></div>
        </div>
    `;

    contentItem.insertAdjacentHTML('beforeend', htmlTemplate);

    const itemsContainer = document.querySelector('.items');
    selectedItem.objects.forEach(object => {
        const htmlInsideTemplate = `
            <div style="margin:15px 0" class="flexed">
                <div style="margin:0 15px">
                    <img class="square-item" src="${object.exImg}" alt="" srcset="">
                    <h2>${object.exName}</h2>
                </div>
                <p>${object.description}</p>
            </div>
        `;
        itemsContainer.insertAdjacentHTML('beforeend', htmlInsideTemplate);
    });
}

